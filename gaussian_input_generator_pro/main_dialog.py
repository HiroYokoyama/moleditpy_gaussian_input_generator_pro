    def save_file(self):
        coord_lines = self._get_active_coord_lines()
        if any("Error" in line for line in coord_lines):
            err = "\n".join(line for line in coord_lines if "Error" in line)
            QMessageBox.critical(self, "Error", f"Coordinate Generation Failed:\n{err}")
            return

        default_dir = os.getcwd()
        default_base = "gaussian_job.gjf"

        if self.filename:
            if os.path.isabs(self.filename):
                default_dir = os.path.dirname(self.filename)
            norm_name = self.filename.replace("\\", "/")
            base_name = norm_name.split("/")[-1]
            if base_name:
                default_base = os.path.splitext(base_name)[0]

        default_base = re.sub(r'[\\/*?:"<>|]', "_", default_base).strip()
        default_base = default_base.lstrip("_")
        if not default_base:
            default_base = "gaussian_job"
        if default_base.lower().endswith((".gjf", ".com")):
            default_base = os.path.splitext(default_base)[0]

        if getattr(self, "auto_suffix_cb", None) and self.auto_suffix_cb.isChecked():
            suffix = self._auto_suffix()
            if suffix and not default_base.endswith(suffix):
                default_base += suffix

        default_base += ".gjf"
        default_full = os.path.join(default_dir, default_base)

        file_path, _ = QFileDialog.getSaveFileName(
            self,
            "Save Gaussian Input",
            default_full,
            "Gaussian Input (*.gjf *.com);;All Files (*)",
        )

        if file_path:
            try:
                content = self.preview_text.toPlainText()
                saved_base = os.path.splitext(os.path.basename(file_path))[0]
                content = self._rewrite_chk(content, saved_base)

                with open(file_path, "w", encoding="utf-8") as f:
                    f.write(content)

                self.preview_text.setText(content)
                self.current_inp_file = file_path
                self._saved_inp_content = content
                self._update_title()
            except Exception as e:
                QMessageBox.critical(self, "Error", f"Failed to save file:\n{str(e)}")

    def _auto_suffix(self):
        kw = self.keywords_edit.toPlainText().upper()
        for suffix, matcher in _AUTO_SUFFIX_RULES:
            if matcher(kw):
                return suffix
        return ""

    @staticmethod
    def _rewrite_chk(content, base_name):
        """Rewrite every %chk= line to use the saved file's basename.

        The --Link1-- section repeats %chk so it can read Job 1's
        checkpoint; all occurrences must stay in sync with the filename.
        """
        chk_line = f"%chk={base_name}.chk"
        if re.search(r"^%chk=.*$", content, re.MULTILINE | re.IGNORECASE):
            content = re.sub(
                r"^%chk=.*$",
                chk_line,
                content,
                flags=re.MULTILINE | re.IGNORECASE,
            )
        return content

