# Priority order used for auto-suffix detection (first match wins).
_AUTO_SUFFIX_RULES = [
    ("-ts", lambda kw: "OPT=(TS" in kw or "OPT=TS" in kw),
    ("-irc", lambda kw: "IRC" in kw),
    ("-scan", lambda kw: "MODREDUNDANT" in kw),
    ("-opt", lambda kw: re.search(r"\bOPT\b", kw) is not None),
    ("-freq", lambda kw: re.search(r"\bFREQ\b", kw) is not None),
    ("-td", lambda kw: re.search(r"\bTD\b", kw) is not None or "TD=" in kw),
    ("-nmr", lambda kw: "NMR" in kw),
    ("-sp", lambda kw: True),
]


